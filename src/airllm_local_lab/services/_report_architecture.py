        "        return os.environ.get('HF_TOKEN')   # NEVER hard-coded\n"
        "    def require_hf_token(self) -> str:\n"
        "        tok = self.hf_token()\n"
        "        if not tok: raise ConfigError('HF_TOKEN missing -- see .env-example')\n"
        "        return tok\n"
        "```\n\n"
        "`.env` git-ignored; `.env-example` has placeholders; tokens never logged "
