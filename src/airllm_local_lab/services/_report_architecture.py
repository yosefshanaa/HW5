        "```\n\n"
        "### 13.3 Backend Strategy (Device-Agnostic)\n\n"
        "| Backend | Role | Device |\n|---|---|---|\n"
        "| `AirLLMBackend` | Layer-streaming via `mmap`; shards on NVMe | CPU (MLX on Apple Silicon) |\n"
